    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            stat=conn.createStatement();
            int Id=Integer.parseInt(memId.getText());
            String sql="SELECT * FROM addmembers WHERE ID='"+Id+"'";

            rs=stat.executeQuery(sql);

            if(rs.next()){
                name.setText(rs.getString("Name"));
                number.setText(rs.getString("Number"));
                mail.setText(rs.getString("Email"));
                address.setText(rs.getString("Address"));
                emerNum.setText(rs.getString("EmergencyNum"));
                memType.setSelectedIndex(rs.getInt("MembershipType"));
                ID.setText(memId.getText());
                receipt.setText(rs.getString("ReceiptNo"));
                pp.setSelectedIndex(rs.getInt("PaymentPlan"));
                g.setText(rs.getString("Gender"));
                h.setText(rs.getString("Height"));
                w.setText(rs.getString("Weight"));
                age.setText(rs.getString("Age"));
                bmi.setText(Double.toString(BMICalc.BMI(rs.getString("Height"),rs.getString("Weight"))));
                rem.setText(rs.getString("Remarks"));
                Admin.setText(rs.getString("AdminName"));
                date.setText(rs.getString("RegDate"));
                lastPaid.setText(rs.getString("LastPaid"));
            }
            else{
                JOptionPane.showMessageDialog(null,"There is No Member with this ID!!!");
            }
        }catch(NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

