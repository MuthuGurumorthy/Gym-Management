    private void showRecords(){
        try{
        
            stat=conn.createStatement();
            String sql="SELECT * FROM addmembers";
            rs=stat.executeQuery(sql);
            DefaultTableModel DefaultTableModel=null;
            jTable1.getModel();
            DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
            Object rowData[]=new Object[6];
            while(rs.next()){
                
                rowData[0]=rs.getString("ID");
                rowData[1]=rs.getString("Name");
                rowData[2]=rs.getString("Number");
                rowData[3]=rs.getString("Address");
                rowData[4]=rs.getString("RegDate");
                rowData[5]=rs.getString("AdminName");
                
                model.addRow(rowData);
            }
            stat.close();
            conn.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,e.getMessage()+"the error");
        }
    }
    
